# function for addition
def add(a, b):
    print(a, '+', b, ' = ', a + b)


# function for minus
def minus(a, b):
    print(a, '-', b, ' = ', a - b)


# function for multiply
def multiply(a, b):
    print(a, 'x', b, ' = ', a * b)


# funtion for Division
def division(a, b):
    print(a, '/', b, ' = ', a / b)

